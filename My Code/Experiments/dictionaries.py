spam = {'color': 'red', 'age':42}


print()
print('printing values')
for v in spam.values(): # prints the values from the dictionary
    print(v)

print()
print('printing keys')
for k in spam.keys(): # prints the keys from the dictionary
    print(k)


print()
print('printing items')
for i in spam.items(): # Prints the keys and their values from the dictionary
    print(i)

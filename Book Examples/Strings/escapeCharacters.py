# \n is an escape character to insert a linebreak
print('Hello there!\nHow are you?\nI\'m doing fine.')

# \ escapes the ' in "Carol's"
print(r'That is Carol\'s cat.')

# Strings within ''' are multiline strings
# all symbols and linebreaks are considered
# to be part of the string. The output will appear like written in the code
print('''Dear Alice,

Eve's cat has been arrested for catnapping, cat burglary, and extortion.

Sincerely,Bob''')


# Prints the same as the above string, but uses line breaks
print('Dear Alice,\n\nEve\'s cat has been arrested for catnapping, cat burglary, and extortion.\n\nSincerely,\nBob')

